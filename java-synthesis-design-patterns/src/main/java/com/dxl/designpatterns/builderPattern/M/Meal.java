package com.dxl.designpatterns.builderPattern.M;

public class Meal {

    private String food;

    private String druck;

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getDruck() {
        return druck;
    }

    public void setDruck(String druck) {
        this.druck = druck;
    }
}
